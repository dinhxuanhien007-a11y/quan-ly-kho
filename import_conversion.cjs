async function run() {
  const entries = Object.entries(conversionData);
  const BATCH_SIZE = 400;
  let total = 0;
  
  for (let i = 0; i < entries.length; i += BATCH_SIZE) {
    const batch = db.batch();
    const chunk = entries.slice(i, i + BATCH_SIZE);
    for (const [id, factor] of chunk) {
      batch.update(db.collection('products').doc(id), { misaConversionFactor: factor });
      total++;
    }
    await batch.commit();
    console.log('Batch done:', Math.min(i + BATCH_SIZE, entries.length), '/', entries.length);
  }
  
  console.log('✅ Xong! Đã cập nhật', total, 'mã hàng');
  process.exit(0);
}

run().catch(err => { console.error(err); process.exit(1); });
